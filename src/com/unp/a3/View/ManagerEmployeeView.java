    private void setTableData() {
        defaultTableModel.setNumRows(0);

        EmployeeModel employeeModel = new EmployeeModel();
        for (Model m : employeeModel.getAll()) {
            EmployeeModel p = (EmployeeModel) m;
            defaultTableModel.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getPosition(),
                p.getLogin(),
                p.getPassword()
            });
        }
    }

