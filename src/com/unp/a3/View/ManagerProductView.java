    private void setTableData() {
        defaultTableModel.setNumRows(0);
        
        ProductModel productModel = new ProductModel();
        for (Model m : productModel.getAll()) {
            ProductModel p = (ProductModel) m;
            defaultTableModel.addRow(new Object[]{
                p.getId(),
                p.getName(), 
                p.getCategory(),
                p.getPrice(),
                p.getQuantity(),
            });
        }
    }

    
    
